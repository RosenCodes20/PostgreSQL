SELECT
    extract(
        'Year'
        FROM
        booked_at
    )
        AS
    "Year",
    extract(
        'Month'
        FROM
        booked_at
    )
        AS
    "Month",
    extract(
        'Day'
        FROM
        booked_at
    )
        AS
    "Day",
    extract(
        'Hour'
        FROM
        booked_at
    )
        AS
    "Hour",
    extract(
        'Minute'
        FROM
        booked_at
    )
        AS
    "Minute",
    ceil(extract(
            'Second'
            FROM
            booked_at
         )
    )
        AS
    "Second"
FROM
    bookings